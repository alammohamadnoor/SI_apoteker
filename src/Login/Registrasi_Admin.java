    public void UserModelTable(){
        
        String nama = txtnmAdmin.getText();
        //String cari = txtcari.getText();
        Object [] Row ={"Nama","Jenis Kelamin","Alamat","No. Telepon","Username", "Password"};
        UserTab = new DefaultTableModel(null, Row);
        try{
            String sql = "SELECT * FROM tb_admin";
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while(rs.next()){
                UserTab.addRow(new Object[]{
                    
                    rs.getString(1), 
                    rs.getString(2), 
                    rs.getString(3), 
                    rs.getString(4), 
                    rs.getString(5), 
                    rs.getString(6)});     
                }
                //tbuser.setModel(UserTab);
            }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(this, "data gagal dipanggil "+ex);
        }
    }    
