    private void bsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bsimpanActionPerformed
        String sql = "insert into tb_admin values (?,?,?,?,?,?)";
        try{
            PreparedStatement st = conn.prepareStatement(sql);
            
            st.setString(1, txtnmAdmin.getText());
            st.setString(2, cbjenkel.getItemAt(cbjenkel.getSelectedIndex()));
            st.setString(3, txtalamat.getText());
            st.setString(4, txtnotelp.getText());
            st.setString(5, txtusername.getText());
            st.setString(6, String.valueOf(txtpassword.getPassword()));            
       
            st.executeUpdate();
            JOptionPane.showMessageDialog(this, "Data Berhasil Disimpan");
            clear();
            txtusername.requestFocus();
            
            
            }catch(SQLException ex){
            JOptionPane.showMessageDialog(this, "Data gagal Disimpan " +ex);
            }        // TODO add your handling code here:
    }//GEN-LAST:event_bsimpanActionPerformed

