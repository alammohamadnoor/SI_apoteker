    private void txtusernameKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtusernameKeyPressed
        usrnm=txtusername.getText();
        int ascii = evt.getKeyCode();
        if(ascii==10){
            try {
                String sql="select * from tb_admin where username='"+ usrnm +"'";
                Statement st=conn.createStatement();
                ResultSet rsusrnm = st.executeQuery(sql);
                while (rsusrnm.next()){
                    txtnmAdmin.setText(rsusrnm.getString(1));
                    cbjenkel.setSelectedItem(rsusrnm.getString(2));
                    txtalamat.setText(rsusrnm.getString(3));
                    txtnotelp.setText(rsusrnm.getString(4));
                    txtpassword.setText(rsusrnm.getString(6));
                    //bdelete.enable(true);
                    //bedit.enable(true);

                }
            } catch (Exception e) {
                JOptionPane.showConfirmDialog(null, "Data Tidak Di Temukan ....!! \n"
                    +e.getMessage());
                txtusername.requestFocus();
            }
        }        // TODO add your handling code here:
    }//GEN-LAST:event_txtusernameKeyPressed

